export const clearLog = (title, varToLog) => {
  if(title && varToLog) {
    console.log(`
      #########################################################
                      ${title}
      #########################################################

    `, varToLog, `

      ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
      #########################################################

    `, `

    `)
  } else {
    console.log(`
      #########################################################
                      ${title}
      #########################################################
    `)
  }

}
